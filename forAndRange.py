#"for" loops and the "range()" function.
#Syntax:
#for var in range(start, stop, step):
#   for clause
#
#Default "start" is "0".
#"stop" is not inclusive.
#Default "step" is "1".

for i in range(0, 10, 2):
    print(i)
for i in range(10, -1, -1):    #countdown from 10 to 0.
    print(i)
